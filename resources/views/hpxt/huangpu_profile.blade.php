    <h1>项目简介</h1>
    <hr>
    <p>2016年计划在全国16个城市，开展基于内分泌进修医生下班后（黄埔夜校）活动打造线下、线下相结合的高端学术服务平台，以帮助内分泌进修医生提高临床治疗水平。项目主要形式为邀请进修医生所在城市知名省级专家轮流作为讲者，针对进修医生感兴趣的专题，进行学术授课。</p>
    <hr>
    <h3>线下部分活动介绍：</h3>
    <ol>
      <li>现场学术授课。</li>
      <li>与全科医学协作平台-远程移动教育“医师助手”软件服务商合作，建立线上“黄埔学堂”学习班级。</li>
     </ol>
    <hr>
    <h3>线上部分介绍：</h3>
    <ol>
      <li>进修医生在手机上安装、注册并加入线上“黄埔导师进修班”后，可国内大专家进行互动交流学习。</li>
      <li>医生也可以将实际工作中遇的问题、病例上传至在线学习班，与同行在线交流。</li>
      <li>每个班级都将配备一名班级助教，以帮助大家解决日常班务活动外，定期还会将班级内的热点问题收集整理为“问答集”交联系导师进行回复。</li>
      <li>班级导师也将定期挑选优秀案例开展专题讲座。所有班级学员都可以参与导师的直播或录播授课学习。</li>
    </ol>
    <hr>
    <h3>项目目的：</h3>
    <p>旨在建立线上和线下相结合的高端学术服务平台，以帮助内分泌进修医生提高 临床治疗水平。</p>
